package Logic;

public class FirmTaxes {
    private String singleTax;
    private String commonTax;
    private String simplyfiedTax;
    private String profitTax;
    private String vat;
    private String insuranceTax;
    private String swf;
    private String incomeTax;

    public String getSingleTax() {
        return singleTax;
    }

    public void setSingleTax(String singleTax) {
        this.singleTax = singleTax;
    }

    public String getCommonTax() {
        return commonTax;
    }

    public void setCommonTax(String commonTax) {
        this.commonTax = commonTax;
    }

    public String getSimplyfiedTax() {
        return simplyfiedTax;
    }

    public void setSimplyfiedTax(String simplyfiedTax) {
        this.simplyfiedTax = simplyfiedTax;
    }

    public String getProfitTax() {
        return profitTax;
    }

    public void setProfitTax(String profitTax) {
        this.profitTax = profitTax;
    }

    public String getVat() {
        return vat;
    }

    public void setVat(String vat) {
        this.vat = vat;
    }

    public String getInsuranceTax() {
        return insuranceTax;
    }

    public void setInsuranceTax(String insuranceTax) {
        this.insuranceTax = insuranceTax;
    }

    public String getSwf() {
        return swf;
    }

    public void setSwf(String swf) {
        this.swf = swf;
    }

    public String getIncomeTax() {
        return incomeTax;
    }

    public void setIncomeTax(String incomeTax) {
        this.incomeTax = incomeTax;
    }
}
