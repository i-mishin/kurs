package Logic;

public class Declare {
    private int declareID;
    private String number;
    private String name;
    private String surname;
    private String title;
    private String nalog;
    private String incomeD;
    private String costsD;
    private String fiscalD;

    public int getDeclareID() {
        return declareID;
    }

    public void setDeclareID(int declareID) {
        this.declareID = declareID;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getNalog() {
        return nalog;
    }

    public void setNalog(String nalog) {
        this.nalog = nalog;
    }

    public String getIncomeD() {
        return incomeD;
    }

    public void setIncomeD(String incomeD) {
        this.incomeD = incomeD;
    }

    public String getCostsD() {
        return costsD;
    }

    public void setCostsD(String costsD) {
        this.costsD = costsD;
    }

    public String getFiscalD() {
        return fiscalD;
    }

    public void setFiscalD(String fiscalD) {
        this.fiscalD = fiscalD;
    }
}
